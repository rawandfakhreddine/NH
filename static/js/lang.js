function setLanguage(lang) {
    localStorage.setItem("siteLanguage", lang);

    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    document.body.classList.toggle("rtl", lang === "ar");

    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    const current = document.getElementById("currentLanguage");
    if (current) {
        current.textContent = lang === "ar" ? "العربية" : "English";
    }
}

document.addEventListener("DOMContentLoaded", function () {
    const saved = localStorage.getItem("siteLanguage") || "en";
    setLanguage(saved);

    const toggle = document.getElementById("languageToggle");
    const menu = document.getElementById("languageMenu");

    if (toggle && menu) {
        toggle.addEventListener("click", function (e) {
            e.stopPropagation();
            menu.classList.toggle("show-language-menu");
        });

        document.addEventListener("click", function () {
            menu.classList.remove("show-language-menu");
        });

        menu.addEventListener("click", function (e) {
            e.stopPropagation();
        });

        document.querySelectorAll(".language-option").forEach((btn) => {
            btn.addEventListener("click", function () {
                const lang = this.getAttribute("data-lang");
                setLanguage(lang);
                menu.classList.remove("show-language-menu");
            });
        });
    }

    const toggleBtn = document.getElementById("chatbotToggle");
    const closeBtn = document.getElementById("chatbotClose");
    const chatBox = document.getElementById("chatbotBox");
    const chatInput = document.getElementById("chatInput");
    const chatMessages = document.getElementById("chatMessages");
    const sendBtn = document.getElementById("chatSendBtn");
    const micBtn = document.getElementById("chatMicBtn");
    const speakBtn = document.getElementById("chatSpeakBtn");

    let lastBotReply = "";
    let recognition = null;
    let isListening = false;

    function getCurrentLang() {
        return localStorage.getItem("siteLanguage") || "en";
    }

    function openChat() {
        if (!chatBox) return;
        chatBox.classList.add("open");
        chatBox.style.display = "flex";
        chatBox.setAttribute("aria-hidden", "false");
        if (chatInput) {
            chatInput.focus();
        }
    }

    function closeChat() {
        if (!chatBox) return;
        chatBox.classList.remove("open");
        chatBox.style.display = "none";
        chatBox.setAttribute("aria-hidden", "true");
    }

    function addMessage(text, type) {
        if (!chatMessages) return null;
        const div = document.createElement("div");
        div.className = `chatbot-message ${type}`;
        div.textContent = text;
        chatMessages.appendChild(div);
        chatMessages.scrollTop = chatMessages.scrollHeight;
        return div;
    }

    function speakText(text) {
        if (!("speechSynthesis" in window) || !text) return;

        window.speechSynthesis.cancel();

        const utterance = new SpeechSynthesisUtterance(text);
        utterance.lang = getCurrentLang() === "ar" ? "ar-SA" : "en-US";
        utterance.rate = 1;
        utterance.pitch = 1;

        window.speechSynthesis.speak(utterance);
    }

    async function sendMessageToServer(message) {
        const typingText = getCurrentLang() === "ar" ? "جاري الكتابة..." : "Typing...";
        const typingDiv = addMessage(typingText, "bot");

        try {
            const response = await fetch("/chat", {
                method: "POST",
                headers: {
                    "Content-Type": "application/json"
                },
                body: JSON.stringify({
                    message: message,
                    lang: getCurrentLang()
                })
            });

            const data = await response.json();
            if (typingDiv) typingDiv.remove();

            const reply = data.response || (getCurrentLang() === "ar" ? "تعذر الحصول على رد." : "Sorry, I could not respond.");
            addMessage(reply, "bot");
            lastBotReply = reply;
            speakText(reply);
        } catch (error) {
            if (typingDiv) typingDiv.remove();

            const errorText = getCurrentLang() === "ar"
                ? "عذرًا، المساعد غير متاح الآن."
                : "Sorry, the assistant is unavailable right now.";
            addMessage(errorText, "bot");
            lastBotReply = errorText;
            speakText(errorText);
        }
    }

    function setupSpeechRecognition() {
        const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
        if (!SpeechRecognition) {
            if (micBtn) {
                micBtn.disabled = true;
                micBtn.title = "Voice input not supported in this browser";
            }
            return;
        }

        recognition = new SpeechRecognition();
        recognition.continuous = false;
        recognition.interimResults = false;

        recognition.onstart = function () {
            isListening = true;
            if (micBtn) {
                micBtn.textContent = "🛑";
            }
        };

        recognition.onend = function () {
            isListening = false;
            if (micBtn) {
                micBtn.textContent = "🎤";
            }
        };

        recognition.onerror = function () {
            isListening = false;
            if (micBtn) {
                micBtn.textContent = "🎤";
            }

            const errorText = getCurrentLang() === "ar"
                ? "تعذر استخدام الميكروفون."
                : "Could not use the microphone.";
            addMessage(errorText, "bot");
        };

        recognition.onresult = async function (event) {
            const transcript = event.results[0][0].transcript;
            if (chatInput) {
                chatInput.value = transcript;
            }

            addMessage(transcript, "user");
            if (chatInput) {
                chatInput.value = "";
            }

            await sendMessageToServer(transcript);
        };
    }

    setupSpeechRecognition();

    window.toggleChat = function () {
        if (!chatBox) return;
        if (chatBox.classList.contains("open")) {
            closeChat();
        } else {
            openChat();
        }
    };

    window.sendMessage = async function () {
        if (!chatInput) return;

        const message = chatInput.value.trim();
        if (!message) return;

        addMessage(message, "user");
        chatInput.value = "";
        await sendMessageToServer(message);
    };

    if (toggleBtn) {
        toggleBtn.addEventListener("click", window.toggleChat);
    }

    if (closeBtn) {
        closeBtn.addEventListener("click", closeChat);
    }

    if (sendBtn) {
        sendBtn.addEventListener("click", window.sendMessage);
    }

    if (micBtn) {
        micBtn.addEventListener("click", function () {
            if (!recognition) return;

            if (isListening) {
                recognition.stop();
                return;
            }

            recognition.lang = getCurrentLang() === "ar" ? "ar-SA" : "en-US";
            recognition.start();
        });
    }

    if (speakBtn) {
        speakBtn.addEventListener("click", function () {
            if (lastBotReply) {
                speakText(lastBotReply);
            } else {
                const fallback = getCurrentLang() === "ar"
                    ? "لا توجد رسالة لقراءتها بعد."
                    : "There is no reply to read yet.";
                speakText(fallback);
            }
        });
    }

    if (chatInput) {
        chatInput.addEventListener("keypress", function (event) {
            if (event.key === "Enter") {
                window.sendMessage();
            }
        });
    }

    if (chatBox) {
        chatBox.style.display = "none";
        chatBox.setAttribute("aria-hidden", "true");
    }
});